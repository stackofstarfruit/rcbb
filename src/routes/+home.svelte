    <p class="text-lg text-gray-700 text-center">Literature Trivia By & For Book Lovers</p>

    <p>Rain City Book Bowl is a joint collaboration between Head in the Clouds Trivia and Quizbowl at the University of Washington. Our goal is to connect book nerds in Seattle by giving them a fun competitive outlet! This league will recur monthly in bookstores around the Seattle area.</p>

    <p>Our inaugural event will be held at Ada's Technical Books and Cafe. You can expect the event to be a hybrid of Head in the Clouds bar trivia and quizbowl. If you're unfamiliar with either or both of these question styles, no worries! We'll explain everything during the event. You can get a more detailed breakdown <a href="/rules">here</a>.</p>
      
    <p>Team size is capped at 4. No teammates? No probem! When you buy your tickets, you can also request to be placed on a team with other players. We hope that this will be a fun way to meet other Seattle readers!</p>

    <p>We will be testing your knowledge of both old and new books, so our event will have an equal amount of questions about each. The genres covered will vary, but you can expect to be asked about classic books, sci-fi / fantasy, romance, poetry, and biography / nonfiction.</p>

    <p>For more information, follow us on Instagram @raincitybookbowl. We hope to see you there!</p>
